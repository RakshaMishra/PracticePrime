package com.kodnest.Project;
import java.util.Scanner;

public class Ques4 {
	public static void main(String[] args) {
		
		int sum=0;
		Scanner sc=new Scanner(System.in);
		int n = sc.nextInt();
		for(int i=1;i<=n;i++)
		{
			sum=sum+i*i;
		
		}
		
		System.out.println("sum is = "+sum);
		
	}
	

	
	
	
	
	
	
	
	
}

		







